# type() is a built-in function in Python that returns the type of an object. It can be used to check the type of a variable or value. Here are some examples:

a = 10 
b = 3.14
c = "Hello, World!"
d = False
print(type(a)) # Output: <class 'int'>
print(type(b)) # Output: <class 'float'>
print(type(c)) # Output: <class 'str'>
print(type(d)) # Output: <class 'bool'>

